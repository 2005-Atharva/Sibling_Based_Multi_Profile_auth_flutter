class Student {
  final String studentId;
  final String name;
  final String email;
  final String contactNo;
  final String avatar;
  final String isSiblingOf;

  Student({
    required this.studentId,
    required this.name,
    required this.email,
    required this.contactNo,
    required this.avatar,
    required this.isSiblingOf,
  });
}
